package com.Notifier;

public class NoteBookObject {

	private int id1;
	private int id2;
	private String s1;
	private int count;

	public NoteBookObject(int id1, int id2, String s1,int count) {
		this.id1=id1;
		this.id2=id2;
		this.s1=s1;
		this.count=count;
	}

	public int getId1() {
		return id1;
	}

	public void setId1(int id1) {
		this.id1 = id1;
	}

	public int getId2() {
		return id2;
	}

	public void setId2(int id2) {
		this.id2 = id2;
	}

	public String getS1() {
		return s1;
	}

	public void setS1(String s1) {
		this.s1 = s1;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}
	

	
	
	
	
}
